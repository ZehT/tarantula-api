CREATE TABLE TARANTULA
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR(50) NOT NULL,
    CONSTRAINT PK_TARANTULA PRIMARY KEY (ID),
    CONSTRAINT UK_01_NAME UNIQUE (NAME)
);

COMMENT ON TABLE TARANTULA IS 'Table that represents an TARANTULA';
COMMENT ON COLUMN TARANTULA.ID IS 'Tarantulas auto-generated ID';
COMMENT ON COLUMN TARANTULA.NAME IS 'Tarantulas name';
